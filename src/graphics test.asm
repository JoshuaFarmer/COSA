YStart = 0

; set to 320x200 256 colours
mov ah, 0
mov al, 0x13
int 10h

; bg colour
mov ah, 0bh
mov bx, 0Fh
int 10h

mov ah, 0ch
mov al, 0
mov bh, 0
mov cx, XStart
mov dx, YStart

mov di, sprite1
call draw

mov ah, 0
int 16h
mov bx, 0x1000   
mov es, bx
mov bx, 0
jmp 0x1000:0000

draw:	; use di for the sprite data
	; draw pixel
	mov ah, 0ch
	mov al, [es:di]
	mov bh, 0
	cmp dx, 8
	jne draw_
ret
draw_:
	inc di
	int 10h
	
	cmp cx, 7
	jz nextY
nextX:
	inc cx
	jmp draw
nextY:
	inc dx
	mov cx, XStart
	jmp draw

times 10 db 0
